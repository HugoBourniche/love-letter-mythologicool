import { UserData } from "../../users/user.data";

export abstract class APlayerData {
  // *****************************************************************************************************************
  // ATTRIBUTES
  // *****************************************************************************************************************

  private _user: UserData;

  // *****************************************************************************************************************
  // CONSTRUCTOR
  // *****************************************************************************************************************

  protected constructor() {
    this._user = new UserData();
  }

  // *****************************************************************************************************************
  // GETTER / SETTER
  // *****************************************************************************************************************

  get user(): UserData {
    return this._user;
  }

  set user(value: UserData) {
    this._user = value;
  }
}
