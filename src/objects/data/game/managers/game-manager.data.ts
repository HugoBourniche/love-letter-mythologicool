import { APlayerData } from "../players/a-player-data";

export abstract class GameManagerData<P extends APlayerData> {
  // *****************************************************************************************************************
  // ATTRIBUTES
  // *****************************************************************************************************************

  private _players: P[];

  // *****************************************************************************************************************
  // CONSTRUCTOR
  // *****************************************************************************************************************

  protected constructor() {
    this._players = [];
  }

  // *****************************************************************************************************************
  // GETTER / SETTER
  // *****************************************************************************************************************

  get players(): P[] {
    return this._players;
  }

  set players(value: P[]) {
    this._players = value;
  }
}
