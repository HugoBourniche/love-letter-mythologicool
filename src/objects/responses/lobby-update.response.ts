import { LobbyDTO } from "../dtos/lobby.dto";

export class LobbyUpdateResponse {
  // *****************************************************************************************************************
  // ATTRIBUTES
  // *****************************************************************************************************************

  private readonly _lobby: LobbyDTO;

  // *****************************************************************************************************************
  // CONSTRUCTOR
  // *****************************************************************************************************************

  constructor(lobby: LobbyDTO) {
    this._lobby = lobby;
  }

  // *****************************************************************************************************************
  // GETTER
  // *****************************************************************************************************************

  get lobby(): LobbyDTO {
    return this._lobby;
  }
}
