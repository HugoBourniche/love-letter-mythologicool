
export class GameOptionRangeData {

    // *****************************************************************************************************************
    // ATTRIBUTES
    // *****************************************************************************************************************

    private _nbPlayersChoices: string[];

    // *****************************************************************************************************************
    // CONSTRUCTOR
    // *****************************************************************************************************************

    constructor(nbPlayersChoices: string[]) {
        this._nbPlayersChoices = nbPlayersChoices;
    }

    // *****************************************************************************************************************
    // GETTER
    // *****************************************************************************************************************

    get nbPlayersChoices(): string[] {
        return this._nbPlayersChoices;
    }
}