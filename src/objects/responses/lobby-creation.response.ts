import {LobbyData} from "../data/lobby.data";

export class LobbyCreationResponse {

    // *****************************************************************************************************************
    // ATTRIBUTES
    // *****************************************************************************************************************

    private _lobby: LobbyData;

    // *****************************************************************************************************************
    // CONSTRUCTOR
    // *****************************************************************************************************************

    constructor(lobby: LobbyData) {
        this._lobby = lobby;
    }

    // *****************************************************************************************************************
    // GETTER
    // *****************************************************************************************************************

    get lobby(): LobbyData {
        return this._lobby;
    }
}